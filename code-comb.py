table = {}


def add_comb(tbl, comb):
    if len(comb) == 1:
        if comb[0] not in tbl:
            tbl[comb[0]] = {}

        tbl[comb[0]]['_'] = 1
    else:
        if comb[0] not in tbl:
            tbl[comb[0]] = {}

        add_comb(tbl[comb[0]], comb[1:])


def print_table(tbl, prefix):
    if len(tbl) == 1:
        print('{} = 1'.format(prefix))
        return

    print(prefix + ' = {}')

    if '_' in tbl:
        print('{}[\'_\'] = 1'.format(prefix))

    for key in sorted(filter(lambda x: x != '_', tbl.keys()), key=symbol2code.codes.index):
        print_table(tbl[key], '{}[\'{}\']'.format(prefix, key))


